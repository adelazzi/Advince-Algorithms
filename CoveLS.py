def cost(s):
    """Calculate the cost as the negative of the number of nodes in the set."""
    return -sum(s)
    
def constraint_violations(s):
    """Count the number of edges between nodes in the selected set."""
    cnt = 0
    for i in range(n):
        for j in range(i+1, n):  # Only check each edge once
            if s[i] == 1 and s[j] == 1 and graph[i][j] == 1:
                cnt += 1
    return cnt 

def initialize_solution():
    """Create an initial solution with all nodes unselected."""
    return [0] * n

def get_neighbor(s):
    """Generate a neighbor solution by flipping one random node."""
    x = np.random.randint(0, n)
    z = s.copy()
    z[x] = 1 - z[x]
    return z
