Niter = 1000

# Initialize solution
s = initialize_solution()
c = cost(s) + constraint_violations(s)

# Local search algorithm
for i in range(Niter):
    sp = get_neighbor(s)
    cp = cost(sp) + constraint_violations(sp)
    
    # Accept if better
    if cp < c:
        c = cp
        s = sp

